#include "3-calc.h"
/**
 * op_add - function that calculates the sum of two integers
 * @a: first integer
 * @b: second integer
 * Return: sum
 */
int op_add(int a, int b)
{
	return (a + b);
}
/**
 * op_sub - function that calculates the difference of two integers
 * @a: first integer
 * @b: second integer
 * Return: difference
 */
int op_sub(int a, int b)
{
	return (a - b);
}
/**
 * op_mul - function that calculates the product of two integers
 * @a: first integer
 * @b: second integer
 * Return: product
 */
int op_mul(int a, int b)
{
	return (a * b);
}
/**
 * op_div - function that calculates the division of two integers
 * @a: first integer
 * @b: second integer
 * Return: division
 */
int op_div(int a, int b)
{
	return (a / b);
}
/**
 * op_mod - function that calculates the %  of the division of two integers
 * @a: first integer
 * @b: second integer
 * Return: %
 */
int op_mod(int a, int b)
{
	return (a % b);
}
